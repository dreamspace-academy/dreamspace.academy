          <p class="template-padding-reset" align="justify">
            The Department of Molecular Science and Biochemistry at the University of Colombo has designed a kit that could assist in reducing the cost for a PCR test. “Our product can be given for one fourth of the price (of imported kits). We are offering it for Rs 80,000,” Professor Ranil Dassanayake who led the team which invented the product told News1st. The new kit will assist in extracting the RNA molecule – which comprises normal human cells and virus cells, from a sample. The PCR test will determine whether there are virus cells in the RNA molecule.</p>
